package com.genome.parpalak.bean;

public class Board {
    
    private int tableId;
    private String name;

    public Board() {
    }

    public int getTableId() {
        return tableId;
    }

    public void setTableId(int tableId) {
        this.tableId = tableId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
   
}
